package Streams;

public class Performer {
	public int id;
	public String name;
	public int problems_solved;
	public String grade;
	public Performer(int id, String name, int problems_solved, String grade) {
		super();
		this.id = id;
		this.name = name;
		this.problems_solved = problems_solved;
		this.grade = grade;
	}


}
